/**
 * 运营账号 用户状态 - 枚举
 * @Author:wudh
 * @Date: 2019/4/21 14:09
 */
public enum AdminUserStatusEnum {

    /** 保密 */
    DISABLE("0", "禁用"),
    /** 男 */
    ENABLE("1", "启用");

    private String code;
    private String msg;

    AdminUserStatusEnum(String code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public String getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }

}
