/**
 * 商品图片类型
 * @Author:wudh
 * @Date: 2019/5/10 15:09
 */
public enum GoodsImgEnum {

    /** 封面图 */
    IMG_TYPE_001(1,"封面图"),
    /** 内容图 */
    IMG_TYPE_002(2,"内容图");

    private Integer code;
    private String msg;

    GoodsImgEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }

}
